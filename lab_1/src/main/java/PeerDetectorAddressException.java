public class PeerDetectorAddressException extends Exception {
    public PeerDetectorAddressException() {
        super();
    }

    public PeerDetectorAddressException(String message) {
        super(message);
    }

    public PeerDetectorAddressException(String message, Throwable cause) {
        super(message, cause);
    }

    public PeerDetectorAddressException(Throwable cause) {
        super(cause);
    }

}

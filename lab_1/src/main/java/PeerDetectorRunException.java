public class PeerDetectorRunException extends Exception {
    public PeerDetectorRunException() {
        super();
    }

    public PeerDetectorRunException(String message) {
        super(message);
    }

    public PeerDetectorRunException(String message, Throwable cause) {
        super(message, cause);
    }

    public PeerDetectorRunException(Throwable cause) {
        super(cause);
    }
}
